        String raza = txtRaza.getText();
        String color = txtColor.getText();
        String observaciones = txtAObserv.getText();
        String alergico = (String) cmbAlergico.getSelectedItem();
        String atenEspe = (String) cmbAtEsp.getSelectedItem();
        
